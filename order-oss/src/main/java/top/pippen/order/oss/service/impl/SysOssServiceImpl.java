package top.pippen.order.oss.service.impl;

import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.core.metadata.IPage;
import org.springframework.stereotype.Service;
import top.pippen.order.common.constant.Constant;
import top.pippen.order.common.page.PageData;
import top.pippen.order.common.service.impl.BaseServiceImpl;
import top.pippen.order.oss.dao.SysOssDao;
import top.pippen.order.oss.entity.SysOssEntity;
import top.pippen.order.oss.service.SysOssService;

import java.util.Map;


@Service
public class SysOssServiceImpl extends BaseServiceImpl<SysOssDao, SysOssEntity> implements SysOssService {

	@Override
	public PageData<SysOssEntity> page(Map<String, Object> params) {
		IPage<SysOssEntity> page = baseDao.selectPage(
			getPage(params, Constant.CREATE_DATE, false),
			new QueryWrapper<>()
		);
		return getPageData(page, SysOssEntity.class);
	}
}
